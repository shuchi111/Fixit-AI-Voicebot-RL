def _json_path(name: str) -> Path:
    return OUTPUTS_DIR / name


@st.cache_data(show_spinner=False)
def load_json(path: str) -> dict[str, Any]:
    file_path = Path(path)
    if not file_path.exists():
        return {}
    return json.loads(file_path.read_text(encoding="utf-8"))


@st.cache_data(show_spinner=False)
def load_parquet(path: str) -> pd.DataFrame:
    file_path = Path(path)
    if not file_path.exists():
        return pd.DataFrame()
    return pd.read_parquet(file_path)


def fmt_float(value: Any, digits: int = 3) -> str:
    if isinstance(value, int | float):
        return f"{value:.{digits}f}"
    return "n/a"


def run_command(command: list[str], timeout_seconds: int = 240) -> tuple[int, str]:
    try:
        completed = subprocess.run(
            command,
            cwd=ROOT,
            capture_output=True,
            text=True,
            timeout=timeout_seconds,
            check=False,
        )
    except subprocess.TimeoutExpired as exc:
        return 124, f"Command timed out after {timeout_seconds}s\n{exc}"
    return completed.returncode, (completed.stdout or "") + (completed.stderr or "")


def metric_delta(value: Any) -> str | None:
    if isinstance(value, int | float):
        return f"{value:+.3f}"
    return None


def show_missing_outputs_help() -> None:
    st.warning(
        "Some output files are missing. Run the full pipeline first: "
        "`uv run python run.py --train --eval --report`"
    )


def render_result_artifact(path: Path) -> None:
    if not path.exists():
        st.info(f"Not generated yet: `{path.relative_to(ROOT)}`")
        return

    st.caption(str(path.relative_to(ROOT)))
    if path.suffix == ".json":
        st.json(load_json(str(path)), expanded=False)
    elif path.suffix == ".parquet":
        frame = load_parquet(str(path))
        st.write(f"Rows: {len(frame):,}, Columns: {len(frame.columns):,}")
        st.dataframe(frame.head(100), use_container_width=True)
    elif path.suffix.lower() in {".png", ".jpg", ".jpeg"}:
        st.image(str(path))
    else:
        st.code(path.read_text(encoding="utf-8"), language="text")


def overview_tab() -> None:
    final_report = load_json(str(_json_path("final_report.json")))
    train_report = load_json(str(_json_path("train_report.json")))
    eval_report = load_json(str(_json_path("eval_report.json")))

    if not final_report:
        show_missing_outputs_help()

    training = final_report.get("training", {})
    offline = final_report.get("offline_evaluation", {})

    st.subheader("Run Summary")
    cols = st.columns(4)
    cols[0].metric("Active Policy", final_report.get("active_policy_version", "n/a"))
    cols[1].metric("Train Calls", training.get("data_split", {}).get("train", "n/a"))
    cols[2].metric("Val Calls", training.get("data_split", {}).get("val", "n/a"))
    cols[3].metric("Test Calls", offline.get("n_test_episodes", "n/a"))

    st.subheader("Does the learned policy improve?")
    cols = st.columns(4)
    cols[0].metric("Baseline Val Return", fmt_float(training.get("baseline_val_return")))
    cols[1].metric(
        "Learned Val Return",
        fmt_float(training.get("learned_val_return")),
        delta=metric_delta(training.get("improvement")),
    )
    cols[2].metric("Baseline Test Return", fmt_float(offline.get("baseline_test_return")))
    cols[3].metric(
        "DR Test Estimate",
        fmt_float(offline.get("dr_estimate")),
        delta=metric_delta(offline.get("improvement_vs_baseline")),
    )

    comparison = final_report.get("comparison_table", [])
    if comparison:
        st.dataframe(pd.DataFrame(comparison), use_container_width=True)

    curve_path = OUTPUTS_DIR / "learning_curve.png"
    if curve_path.exists():
        st.image(str(curve_path), caption="Learning curve generated by training")

    st.subheader("Quick Run Commands")
    st.code(
        "\n".join(
            [
                "uv sync",
                "uv run pytest -q",
                "uv run python run.py --train --eval --report",
                "uv run streamlit run streamlit_app.py",
            ]
        ),
        language="bash",
    )

    with st.expander("System capabilities"):
        capabilities = pd.DataFrame(
            [
                ("End-to-end pipeline", "uv run python run.py --train --eval --report"),
                ("Learning improvement", "outputs/train_report.json"),
                ("Learning curve", "outputs/learning_curve.png"),
                ("Offline evaluation", "outputs/eval_report.json"),
                ("Human control", "--inspect, --freeze, --rollback"),
                ("Design document", "docs/DESIGN.md"),
            ],
            columns=["Capability", "Where to find it"],
        )
        st.dataframe(capabilities, use_container_width=True, hide_index=True)

