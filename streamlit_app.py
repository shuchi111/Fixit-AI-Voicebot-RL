def reports_tab() -> None:
    action_report = load_json(str(_json_path("action_report.json")))
    reward_report = load_json(str(_json_path("reward_report.json")))
    eval_report = load_json(str(_json_path("eval_report.json")))
    train_report = load_json(str(_json_path("train_report.json")))

    if not any([action_report, reward_report, eval_report, train_report]):
        show_missing_outputs_help()
        return

    st.subheader("Action Distribution")
    action_distribution = action_report.get("action_distribution", {})
    if action_distribution:
        action_df = (
            pd.DataFrame(action_distribution.items(), columns=["Action", "Count"])
            .sort_values("Count", ascending=False)
            .set_index("Action")
        )
        st.bar_chart(action_df)
        st.dataframe(action_df.reset_index(), use_container_width=True, hide_index=True)

    st.subheader("Reward Components")
    reward_counts = reward_report.get("reward_component_counts", {})
    if reward_counts:
        reward_df = (
            pd.DataFrame(reward_counts.items(), columns=["Reward component", "Count"])
            .sort_values("Count", ascending=False)
            .set_index("Reward component")
        )
        st.bar_chart(reward_df)
        st.dataframe(reward_df.reset_index(), use_container_width=True, hide_index=True)

    st.subheader("Offline Evaluation")
    if eval_report:
        rows = []
        for key in ["ips", "dr"]:
            result = eval_report.get(key, {})
            if result:
                ci = result.get("ci_95", [None, None])
                rows.append(
                    {
                        "Estimator": result.get("estimator", key.upper()),
                        "Point estimate": result.get("point_estimate"),
                        "CI low": ci[0],
                        "CI high": ci[1],
                        "Episodes": result.get("n_episodes"),
                    }
                )
        if rows:
            st.dataframe(pd.DataFrame(rows), use_container_width=True, hide_index=True)

        diagnostics = eval_report.get("dr", {}).get("diagnostics", {})
        if diagnostics:
            cols = st.columns(3)
            cols[0].metric(
                "Matched Action Rate",
                fmt_float(diagnostics.get("matched_action_rate")),
            )
            cols[1].metric(
                "Low Propensity Fraction",
                fmt_float(diagnostics.get("low_propensity_fraction")),
            )
            cols[2].metric(
                "IPS Nonzero Episodes",
                diagnostics.get("ips_nonzero_weight_episodes", "n/a"),
            )

    st.subheader("Learning Curve Data")
    curve = train_report.get("learning_curve", [])
    if curve:
        curve_df = pd.DataFrame(curve).set_index("epoch")
        st.line_chart(curve_df)
        st.dataframe(curve_df.reset_index(), use_container_width=True, hide_index=True)


def policy_tab() -> None:
    registry = load_json(str(POLICIES_DIR / "registry.json"))
    policy_path = POLICIES_DIR / registry.get("active_file", "policy_active.json")

    st.subheader("Policy Registry")
    if registry:
        st.json(registry, expanded=False)
    else:
        st.warning("No registry found. Run `uv run python run.py --train` first.")

    st.subheader("Active Policy Inspection")
    if not policy_path.exists():
        st.warning(f"Active policy not found: {policy_path}")
        return

    policy = load_policy(policy_path)
    inspection = inspect_policy(policy)

    cols = st.columns(3)
    cols[0].metric("Version", inspection.version)
    cols[1].metric("State Clusters", inspection.num_states)
    cols[2].metric("Policy File", policy_path.name)

    if inspection.action_distribution:
        dist_df = (
            pd.DataFrame(
                inspection.action_distribution.items(),
                columns=["Action", "Selected states"],
            )
            .sort_values("Selected states", ascending=False)
            .set_index("Action")
        )
        st.bar_chart(dist_df)
        st.dataframe(dist_df.reset_index(), use_container_width=True, hide_index=True)

    if inspection.top_states:
        st.subheader("Top Q States")
        st.dataframe(
            pd.DataFrame(inspection.top_states[:25]),
            use_container_width=True,
            hide_index=True,
        )

    policy_data = load_json(str(policy_path))
    freeze_rules = policy_data.get("freeze_rules")
    with st.expander("Freeze rules stored in active policy"):
        st.json(freeze_rules or {}, expanded=True)


def control_tab() -> None:
    st.subheader("Human Control Commands")
    st.markdown(
        """
        These commands are the human gate for the learned policy. They do not require live users.
        """
    )

    commands = {
        "Inspect active policy": ["uv", "run", "python", "run.py", "--inspect"],
        "Diff baseline vs learned": [
            "uv",
            "run",
            "python",
            "run.py",
            "--inspect",
            "--diff",
            "baseline",
            "learned_v1",
        ],
        "Apply freeze rules": [
            "uv",
            "run",
            "python",
            "run.py",
            "--freeze",
            "configs/freeze_rules.yaml",
        ],
        "Rollback to baseline": [
            "uv",
            "run",
            "python",
            "run.py",
            "--rollback",
            "baseline",
        ],
        "Restore learned policy": [
            "uv",
            "run",
            "python",
            "run.py",
            "--rollback",
            "learned_v1",
        ],
    }

    for label, command in commands.items():
        with st.expander(label):
            st.code(" ".join(command), language="bash")
            if st.button(f"Run: {label}", key=label):
                with st.spinner(f"Running {label}..."):
                    code, output = run_command(command, timeout_seconds=90)
                if code == 0:
                    st.success("Command completed")
                else:
                    st.error(f"Command failed with exit code {code}")
                st.code(output or "(no output)", language="text")

    st.subheader("Full Pipeline")
    st.code("uv run python run.py --train --eval --report", language="bash")
    if st.button("Run full training + evaluation pipeline"):
        with st.spinner("This may take 30-90 seconds..."):
            code, output = run_command(
                ["uv", "run", "python", "run.py", "--train", "--eval", "--report"],
                timeout_seconds=300,
            )
        if code == 0:
            st.success("Full pipeline completed. Refresh the dashboard to reload cached reports.")
            st.cache_data.clear()
        else:
            st.error(f"Pipeline failed with exit code {code}")
        st.code(output or "(no output)", language="text")


